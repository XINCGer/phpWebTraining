<p>&nbsp;&nbsp;&nbsp;&nbsp;<font size="7" face="楷体" color="red">这个夜晚，当西安体育场里整整4.6万名球迷一起高唱国歌时，任何人身在现场，情绪都会澎湃起伏，让人难以平静。</font></p>
